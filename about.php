<p><h1><center>OUR STORY</h1></center></br></br>
	     Owner, Varinder Singh, opened the restaurant in 1995, with the idea of creating a space where Indian food could be devoured within the respected setting it deserved: white table clothes, fine wine and a decor that highlights the rich cultural and artistic history of his native India.
	 Having previously been a collector and dealer of Indian art and antiques, his keen eye for interesting pieces has made its impression on the ambiance of the restaurant, where most of the artwork is one of a kind (including a piece from the Indian Pavilion at Expo ’67!).
		</br></br>
	It is with this same passion that Mr Singh & Mr. Narang, who can still be seen buzzing around the dining room on a busy night, continues to maintain the high quality and exceptional service to old and new clients alike. 
	Over the years, our hotel & restaurant has become a staple for many Montrealers and international clients, with a number of patrons having grown up dining with us and coming back over time with families of their own. 
	In that respect, our own family of dedicated staff members - many of whom have been with us since the very beginning - are in large part how we have built our reputation of excellence for over 20 years. 
	With a large number of our regular patrons making up a who's who, and having been as loyal to us as we have been to them and to our classical recipes, we hope to continue our journey in the mastery of culinary arts and service for many years to come.
 
